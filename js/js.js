//background random color-colorchanger
const htmlBody = document.querySelector("body");

const randomClickFunction = function() {
	const colors = ["#EC7272","#850E35","#850E35","#C1D5A4","#8FBDD3","#9FC088","#CC9544","#9D5353"];
	
	const randomIndex = Math.floor(Math.random() * colors.length);
	
	const randomColor = colors[randomIndex];
	
	htmlBody.style.background = randomColor;
	
	
}
htmlBody.onclick = randomClickFunction;//background random color-colorchanger
